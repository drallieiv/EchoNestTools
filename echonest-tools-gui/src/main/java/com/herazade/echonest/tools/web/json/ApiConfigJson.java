package com.herazade.echonest.tools.web.json;

public class ApiConfigJson {

	private String apiKey;

	/**
	 * @return the apiKey
	 */
	public String getApiKey() {
		return apiKey;
	}

	/**
	 * @param apiKey
	 *            the apiKey to set
	 */
	public ApiConfigJson setApiKey(String apiKey) {
		this.apiKey = apiKey;
		return this;
	}

}
